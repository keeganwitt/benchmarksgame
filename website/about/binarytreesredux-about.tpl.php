<p>The binary-trees-redux benchmark is another simplistic adaptation of <a href="http://www.hpl.hp.com/personal/Hans_Boehm/gc/gc_bench/applet/GCBench.java">Hans Boehm's GCBench</a>,
which in turn was adapted from a benchmark by John Ellis and Pete Kovac.</p>

<p>Heap size has been limited for each program, to 8KB more than that program needs to complete the task.</p>
